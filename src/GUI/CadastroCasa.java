    private void verificarCampos() {
        boolean todosPreenchidos
                = !inputRua.getText().trim().isEmpty()
                && !inputNumero.getText().trim().isEmpty()
                && !inputBairro.getText().trim().isEmpty()
                && !inputCidade.getText().trim().isEmpty()
                && !inputDataConstrucao.getText().trim().isEmpty()
                && !inputAreaTotal.getText().trim().isEmpty()
                && !inputAreaConstruida.getText().trim().isEmpty()
                && !inputQtdDormitorios.getText().trim().isEmpty()
                && !inputQtdBanheiros.getText().trim().isEmpty()
                && !inputQtdVagas.getText().trim().isEmpty()
                && !inputIPTU.getText().trim().isEmpty()
                && !inputVenda.getText().trim().isEmpty()
                && !inputAluguel.getText().trim().isEmpty();
        buttonEnviar.setEnabled(todosPreenchidos);
    }

    private void addTextFieldListeners() {
        DocumentListener listener = new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                verificarCampos();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                verificarCampos();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                verificarCampos();
            }
        };

        
        inputRua.getDocument().addDocumentListener(listener);
        inputNumero.getDocument().addDocumentListener(listener);
        inputBairro.getDocument().addDocumentListener(listener);
        inputCidade.getDocument().addDocumentListener(listener);
        inputDataConstrucao.getDocument().addDocumentListener(listener);
        inputAreaTotal.getDocument().addDocumentListener(listener);
        inputAreaConstruida.getDocument().addDocumentListener(listener);
        inputQtdDormitorios.getDocument().addDocumentListener(listener);
        inputQtdBanheiros.getDocument().addDocumentListener(listener);
        inputQtdVagas.getDocument().addDocumentListener(listener);
        inputIPTU.getDocument().addDocumentListener(listener);
        inputVenda.getDocument().addDocumentListener(listener);
        inputAluguel.getDocument().addDocumentListener(listener);
    }
    
